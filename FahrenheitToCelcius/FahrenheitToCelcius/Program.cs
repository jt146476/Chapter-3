using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace FahrenheitToCelcius
{
    class Program
    {
        static void Main(string[] args)
        {
            int entry;
            int celcius;
            int temp;

            Console.WriteLine("convert the temp to celcius");
            entry = Convert.ToInt32(Console.ReadLine());

            celcius = entry f-32
            temp = entry 5/9

            Console.WriteLine("{0} entered {1}       {2} ", entry, celcius, temp);
        }
    }
}
